// Function to filter cities based on search term
export function filterCities(searchTerm) {
  if (!searchTerm) return israelCities;
  const term = searchTerm.toLowerCase();
  return israelCities.filter(city => 
    city.toLowerCase().includes(term) || 
    city.replace(/[״׳]/g, '').toLowerCase().includes(term)
  );
}